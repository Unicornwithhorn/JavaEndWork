public class TypeParameter {
    String type;
    String russianNameSingle;
    String russianNamePlural;
    int numberParameter;


    public TypeParameter(String type, String russianNameSingle, String russianNamePlural, int numberParameter) {
        this.type = type;
        this.russianNameSingle = russianNameSingle;
        this.russianNamePlural = russianNamePlural;
        this.numberParameter = numberParameter;
    }
}
